package br.edu.fcsl.entidade;

public class DadosTeste {
	
	private String ip;
	private String enviados;
	private String recebidos;
	private String perdidos;
	private String porcentagem;
	private String status;
	
	
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getEnviados() {
		return enviados;
	}
	public void setEnviados(String enviados) {
		this.enviados = enviados;
	}
	public String getRecebidos() {
		return recebidos;
	}
	public void setRecebidos(String recebidos) {
		this.recebidos = recebidos;
	}
	public String getPerdidos() {
		return perdidos;
	}
	public void setPerdidos(String perdidos) {
		this.perdidos = perdidos;
	}
	public String getPorcentagem() {
		return porcentagem;
	}
	public void setPorcentagem(String porcentagem) {
		this.porcentagem = porcentagem;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
		

}
